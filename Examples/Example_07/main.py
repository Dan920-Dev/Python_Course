#Lists and SubLists

listPrincipal = ["Monday","Tuesday","Wednesday","Thursday","Friday","Saturday", "Sunday"]
subList = listPrincipal[2:4] # The first 4 elements
print(subList)

subList = listPrincipal[3:6] # Select from Thursday to Saturday
print(subList)

subList = listPrincipal[0:7:2] # list jump 2 by 2
print(subList)