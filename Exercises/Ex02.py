# Convert from dollar to colón

"""
The colón has been a monetary unit of El Salvador since 1892. 
It was progressively replaced by the US dollar in 2001. 
The colón was issued since 1934 by the Central Reserve Bank of El Salvador
"""

print("Enter the amount in dollars you want to convert to colones: ")
convertDollar = float(input())

result = convertDollar * 8.75

print(convertDollar, " Dollars in colones is: ", result)